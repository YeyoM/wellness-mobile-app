export function timeToMilliseconds(date) {
  return date.getTime();
}

// common units in milliseconds
export const MILLISECONDS_IN_A_SECOND = 1000;

export const MILLISECONDS_IN_A_MINUTE = 60000;

export const MILLISECONDS_IN_AN_HOUR = 3600000;

export const MILLISECONDS_IN_A_DAY = 86400000;

export const MILLISECONDS_IN_A_WEEK = 604800000;

export const MILLISECONDS_IN_A_MONTH = 2628000000;

export const MILLISECONDS_IN_A_YEAR = 31540000000;
